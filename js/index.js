//Open Item Mien Bac
$(document).ready(function(){
    $("#item-details-mb-nc4").click(function(){
        $(("#show-item-details-mb-np")).hide();
        $(("#show-item-details-mb-bt")).hide();
      $("#show-item-details-mb-nc4").slideToggle("slow");
     
    });
  });
  $(document).ready(function(){
    $("#item-details-mb-np").click(function(){
        $(("#show-item-details-mb-nc4")).hide();
        $(("#show-item-details-mb-bt")).hide();
      $("#show-item-details-mb-np").slideToggle("slow");
  
    });
  });
  $(document).ready(function(){
    $("#item-details-mb-bt").click(function(){
        $(("#show-item-details-mb-nc4")).hide();
        $(("#show-item-details-mb-np")).hide();
      $("#show-item-details-mb-bt").slideToggle("slow");
  
    });
  });
